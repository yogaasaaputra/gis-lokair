            <p><strong>Sejarah PDAM Tirta Patriot Kota Bekasi :</strong></p>
            <ul>
                <li>Didasari besarnya tuntutan masyarakat terhadap kebutuhan air bersih, maka dibentuklah Satuan Pengelolaan Instalasi Air Bersih (SP-IPAS).</li>
                <li>Pendiriannya diawali dari proyek WEST JAVA URBAN DEVELOPMENT SECTOR PROJECT (WJ-UDSP) ADB LOAN 1384-INO tahun 2001 - 2003 </li>
                <li>dengan sumber dana dari pinjaman ASIA DEVELOPMENT BANK (ADB), Pemerintah Pusat dan Pemerintah Kota Bekasi. Tujuannya adalah untuk </li>
                <li>menyediakan air bersih bagi masyarakat di daerah rawan air bersih.</li>
                </p>
                <li>Setelah proyek selesai dilaksanakan, pengelolaan hasil pembangunan diserahkan kepada Satuan Pengelola Instalasi Air Bersih (SP-IPAS) </li>
                <li>sesuai dengan SK. Walikota Bekasi Nomor 693/DTKP/X/2003. Pengelolaan yang cukup baik menjadikan SP-IPAS dipercaya sebagai penyedia </li>
                <li>layanan air bersih. Jumlah pelanggan yang terus bertambah menuntut pengembangan manajemen dan organisasi.Oleh karena itu, SP-IPAS </li>
                <li>disempurnakan menjadi Pengelola Instalasi Pengelolaan Air Teluk Buyung (PIPA TB) berdasarkan keputusan Walikota Nomor 71 Tahun 2004</li>
                <li>tentang Pembentukan Organisasi dan Pengelolaan Kepegawaian serta Keuangan Instalasi Pengelolaan Air Teluk Buyung Kota Bekasi.</li>
                </p>
                <li>Manajemen perusahaan yang semakin baik menjadikan PIPA TB kian penting perannya di Kota Bekasi. Kemampuan memberikan sumbangan </li>
                <li>bagi PAD Kota Bekasi menjadi acuan penyempurnaan organisasi menuju PDAM Tirta Patriot Kota Bekasi melalui Peraturan Daerah </li>
                <li>Kota Bekasi Nomor 02 Tahun 2006.</li>
                </p>
                <li>Dari perjalanan yang belum lama, PDAM Tirta Patriot Kota Bekasi mengalami kemajuan yang signifikan baik secara manajemen,</li>
                <li>keuangan, pelayanan, serta jumlah pelanggan. Hal itu membuktikan bahwa PDAM Tirta Patriot mampu menjadi penyumbang</li>
                <li>Pendapatan Asli Daerah (PAD) bagi Pemerintah Kota Bekasi dan mampu melayani kebutuhan air bersih untuk masyarakat</li>
                <li>secara baik, handal serta profesional. Dengan target yang realistis, kebutuhan seluruh masyarakat akan air minum</li>
                <li>optimis dapat dicapai.</li>
